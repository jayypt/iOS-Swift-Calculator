import UIKit

class subC: NSObject {
    func plus(_ b: Float,o:Float)->Float{
       return b+o
    }
    func minus(_ b: Float,o:Float)->Float{
       return b-o
    }
    func multiply(_ b: Float,o:Float)->Float{
        return b*o
    }
    func divide(_ b: Float,o:Float)->Float{
        return b/o
    }

}
